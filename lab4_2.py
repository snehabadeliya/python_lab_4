# Create a dictionary emp
emp = {'id': 1, 'name': 'John', 'designation': 'Software Engineer', 'salary': 50000}

# Print the original dictionary
print("Original dictionary:", emp)

# Delete the 'designation' key
del emp['designation']

# Print the dictionary after deleting the 'designation' key
print("Dictionary after deleting 'designation' key:", emp)

# Update the 'name' key
emp['name'] = 'Jane'

# Print the dictionary after updating the 'name' key
print("Dictionary after updating 'name' key:", emp)
